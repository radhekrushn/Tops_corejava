package operators;

public class O003_Relational {


	public static void main(String[] args) {
		
		int a = 10;
		int b = 20;
		int c = 30;
		
		System.out.println(a<b); // true
		System.out.println(a>b); // false
		System.out.println(a==c); // false
		System.out.println(a!=b); // true
		System.out.println(a>=c); // false
		System.out.println(a<=b); // true
		
		
		
		
		
		
		
		
		
		
		
		
		


	}

}
