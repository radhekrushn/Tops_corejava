package Self_Learning;

public class Odd_Even_Number {


	public static void main(String[] args) {
		
		int num = 100;
		
		for(int a=1;a<=num;a++)
		{
		 if(a%2==0)
		 {
			 System.out.println(a+": Even number ");
		 }
		 else
		 {
			 System.out.println(a+": Odd number");
		 }
		}
		

	}

}
